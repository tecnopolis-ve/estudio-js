/* dada una temperatura especificada en grados fahrenheit, mostrar el resultado de convertir el valor introduciido a grados Celsius. Fórmula para la conversión: 

C = (F - 32) / 1.8 */

function celsiusToFahrenheit (celsius) {
    return (celsius * 1.8) + 32; 
}
function fahrenheitToCelsius (fahrenheit) {
    return (fahrenheit - 32) / 1.8; 
}

